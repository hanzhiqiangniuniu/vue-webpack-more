<script type="text/ecmascript-6">
  import $ from 'jquery';
  import '../../common/css/reset.min.css'
  import '../../common/css/service.css'
  import nav from '../public/nav/nav'
  import footer from '../public/footer/footer'
  import gotop from '../public/goTop/serviceGotop'
  import board from '../public/board/serviceBoard';
  export default{
    components:{
      'v-board':board,
      'v-footer':footer,
      'v-nav':nav,
      'v-goTop':gotop,
    },
    data () {
      return {
        scrollWatch: true
      }
    },
    mounted(){
      var oQuesTab=document.getElementById('quesTab'),
        aQLi=oQuesTab.getElementsByTagName('li'),
        oAnTab=document.getElementById('anTab'),
        aAli=oAnTab.getElementsByTagName('li');
      $(window).on('scroll', () => {
        if(this.scrollWatch){
          var anListTop=$('.lastAnlist').offset().top - $(window).scrollTop();
          var firstT=$('.firstT').offset().top - $(window).scrollTop();
          for(var i=0;i<aAli.length;i++){
            var wst =  $(window).scrollTop();
            for (i=1; i<5; i++){
              var anOffset=$("#A"+i).offset().top;
              if(anOffset-64 < wst){ //判断滚动条位置
                $('#quesTab li').removeClass("bg"); //清除c类
                $("#Q"+i+i).addClass("bg");	//给当前导航加c类
              }
            }
          }
          if(firstT<=0){
            $('.list_Box').css({
              position:'fixed',
              top:'8px',
              'margin-top':'0'
            })
          }else if(firstT > 0){
            $('.list_Box').css({
              position:'static',
              top:'inherit'
            })
          }
          if(anListTop <=0){
            $('.list_Box').css({
              position:'static',
              top:'inherit',
              'margin-top':'400px'
            });
          }
        }
      });
      for(var i=0; i<aQLi.length; i++){
        aQLi[i].index=i;
        aQLi[i].onclick=function(){
          for(var i=0; i<aQLi.length; i++){
            aQLi[i].className='quesList';
          }
          this.className='quesList bg';
          $(window).scrollTop(aAli[this.index].offsetTop);
        }
      }
    },
    destroyed () {
      this.scrollWatch = false;
    }
  }
</script>

